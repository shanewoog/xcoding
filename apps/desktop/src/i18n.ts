const catalogs: Record<Locale, Record<MessageKey, string>> = {
  en: en as Record<MessageKey, string>,
  "zh-CN": zhCN,
};

export function isLocale(value: string | null | undefined): value is Locale {
  return value === "en" || value === "zh-CN";
}

export function detectLocale(preferred?: string | null): Locale {
  const source = preferred || (typeof navigator !== "undefined" ? navigator.language : "en");
  const normalized = source.trim().toLowerCase();
  if (normalized === "zh" || normalized.startsWith("zh-")) return "zh-CN";
  return "en";
}

export function loadLocale(): Locale {
  try {
    if (typeof localStorage !== "undefined") {
      const stored = localStorage.getItem(LOCALE_STORAGE_KEY);
      if (isLocale(stored)) return stored;
    }
  } catch {
    // ignore storage access failures
  }
  return detectLocale();
}

export function saveLocale(locale: Locale): void {
  try {
    if (typeof localStorage !== "undefined") {
      localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    }
  } catch {
    // ignore storage access failures
  }
}

export function t(
  locale: Locale,
  key: MessageKey,
  vars?: Record<string, string | number>,
): string {
  const catalog = catalogs[locale] ?? catalogs.en;
  let text = catalog[key] ?? catalogs.en[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}

export function localeDisplayName(locale: Locale): string {
  return locale === "zh-CN" ? t(locale, "lang.zhCN") : t(locale, "lang.en");
}
